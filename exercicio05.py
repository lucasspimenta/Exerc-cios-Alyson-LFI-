"""
Faça um algoritmo que receba dois números e ao final mostre a soma, subtração, multiplicação e a divisão dos números lidos. 
"""

a = int(input('Digite o primeiro número: '))
b = int(input('Digite o segundo número: '))

soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b

print(f'A soma entre os números é: {soma}; A subtração entre os números é {subtracao}; A multiplicação entre os números é: {multiplicacao}; A divisão entre os números é: {divisao}.')