"""
Efetuar a leitura de dois valores numéricos inteiros representados pelas variáveis A e B e apresentar o resultado da diferença A-B. 
"""

a = int(input('Digite o primeiro valor: '))
b = int(input('Digite o segundo valor: '))

subtracao = a - b

print(f'O resultado de {a} - {b} é: {subtracao}')
